<script>
import Main from "./layouts/main.vue";

export default {
    name: "Index",
    components: {
        Main,
    },
    data() {
        return {
            cars: [],
            pagination: {
                current_page: 1,
                last_page: 1,
                next_page_url: null,
                prev_page_url: null,
                path: "",
            },
        };
    },
    mounted() {
        this.fetchData(1);
    },
    computed: {
        totalPages() {
            // Generate an array of page numbers for pagination
            if (!this.pagination || !this.pagination.total_pages) return [];
            return Array.from(
                { length: this.pagination.total_pages },
                (_, i) => i + 1
            );
        },
    },
    methods: {
        async fetchData(page) {
            try {
                const response = await fetch(
                    `http://127.0.0.1:8000/api/cars?page=${page}`
                );
                const data = await response.json();
                this.cars = data.data;

                this.pagination = {
                    current_page: data.current_page,
                    last_page: data.last_page,
                    next_page_url: data.next_page_url,
                    prev_page_url: data.prev_page_url,
                    path: data.path,
                    total_pages: data.last_page, // Using `last_page` as total_pages
                };
            } catch (error) {
                console.error("Error fetching data:", error);
            }
        },
    },
};
</script>
